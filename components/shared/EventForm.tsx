import { createEvent, updateEvent } from "@/lib/actions/event.actions";
import { IEvent } from "@/lib/mongodb/database/models/event.model";

type EventFormProps = {
  userId: string;
  type: "Crear" | "Actualizar";
  event?: IEvent;
  eventId?: string;
};

